using System.Collections;
using System.Collections.Generic;
using UnityEngine;

// ----------------------------------------------------------------------------------------------------------------------------------------------------------
// Public functions
// ----------------------------------------------------------------------------------------------------------------------------------------------------------
public interface _XRPlayerCollisions
{
}
// ----------------------------------------------------------------------------------------------------------------------------------------------------------



// ----------------------------------------------------------------------------------------------------------------------------------------------------------
// Main class
// ----------------------------------------------------------------------------------------------------------------------------------------------------------
public class XRPlayerCollisions : MonoBehaviour, _XRPlayerCollisions
{
    // ------------------------------------------------------------------------------------------------------------------------------------------------------
    // Public variables
    // ------------------------------------------------------------------------------------------------------------------------------------------------------
    public XRCameraMover cameraMover;
    // ------------------------------------------------------------------------------------------------------------------------------------------------------



    // ------------------------------------------------------------------------------------------------------------------------------------------------------
    // Private variables
    // ------------------------------------------------------------------------------------------------------------------------------------------------------
    // ------------------------------------------------------------------------------------------------------------------------------------------------------



    // ------------------------------------------------------------------------------------------------------------------------------------------------------
    // What to do when we collide with stuff
    // ------------------------------------------------------------------------------------------------------------------------------------------------------
    private void OnTriggerEnter(Collider other)
    {
        if (cameraMover != null) cameraMover.startCollided(other);
    }
    private void OnTriggerStay(Collider other)
    {
        if (cameraMover != null) cameraMover.stillCollided(other);
    }
    private void OnTriggerExit(Collider other)
    {
        if (cameraMover != null) cameraMover.stopCollided(other);
    }
    // ------------------------------------------------------------------------------------------------------------------------------------------------------
}
